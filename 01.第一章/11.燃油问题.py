#在美国，车辆的燃油效率通常以每加仑英里数（MPG）表示。在
#加拿大，燃油效率通常以每百公里升数（L/100公里）来表示。使用研究技能来决定如何从MPG转换到L/100公里。然后创建一个程序，从用户那里读取一个美国单位的值，用加拿大单位显示等效的燃油效率。
# 定义转换常数
# 这个常数是根据 1 加仑 = 3.785411784 升 和 1 英里 = 1.609344 公里 推导出来的
# 公式是：L/100km = (100 * 升/加仑) / (公里/英里 * MPG)
# 简化后约为 235.21458
CONVERSION_FACTOR = 235.21458

print("--- 燃油效率单位转换器：MPG (美国) 转 L/100公里 (加拿大) ---")

while True:
    try:
        # 提示用户输入以 MPG 为单位的燃油效率
        mpg_input_str = input("请输入车辆的燃油效率 (MPG，例如：30)：")

        # 将用户输入转换为浮点数
        mpg_value = float(mpg_input_str)

        # 检查输入的 MPG 值是否有效（不能为负或零，因为 MPG 表示每加仑行驶的距离，不能是负的或无限的）
        if mpg_value <= 0:
            print("输入错误：MPG 值必须是一个正数。请重新输入。")
        else:
            # 使用公式将 MPG 转换为 L/100公里
            liters_per_100km = CONVERSION_FACTOR / mpg_value

            # 显示转换结果，并保留两位小数，让结果更易读
            print(f"等效的加拿大燃油效率是：{liters_per_100km:.2f} L/100公里")
            break  # 转换成功，退出循环

    except ValueError:
        # 如果用户输入了非数字字符，捕获 ValueError
        print("输入错误：请输入一个有效的数字。请重新输入。")
    except Exception as e:
        # 捕获其他可能的错误
        print(f"发生了一个意外错误：{e}")

print("--------------------------------------------------")
