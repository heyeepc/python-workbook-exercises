#本练习将创建一个程序，该程序首先读取用户的英尺数据。然后，程序应该以英寸、码和英里显示相同的距离。如果没有记住必要的转换因子，可以上网查找。
# 读取用户输入的英尺数，并转换为浮点数
feet = float(input("请输入距离（英尺）："))

# 各种单位的换算
inches = feet * 12            # 1 英尺 = 12 英寸
yards  = feet / 3             # 1 码 = 3 英尺
miles  = feet / 5280          # 1 英里 = 5280 英尺

# 输出结果，保留两位小数
print(f"{feet:.2f} 英尺 等于：")
print(f"  {inches:.2f} 英寸")
print(f"  {yards:.2f}  码")
print(f"  {miles:.6f} 英里")
