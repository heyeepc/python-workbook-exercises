#一个月的长度从28天到31天不等。本练习将创建一个程序，从用户那里读取一个月的名称作为字符串。然后程序应该显示该月的天数。给2月份显示“28或29天”，这样闰年就有了。
# 使用英文月份名称或缩写作为键，因为它们是字符串，更符合题目要求
month_days_names = {
    "一月": "31",
    "二月": "28或29", 
    "三月": "31",
    "四月": "30",
    "五月": "31",
    "六月": "30",
    "七月": "31",
    "八月": "31",
    "九月": "30",
    "十月": "31",
    "十一月": "30",
    "十二月": "31"
}

# 读取月份名称作为字符串
month_name = input("请输入月份名称 (例如：一月, 二月, ...): ")

# 检查输入的月份名称是否在字典的键中
if month_name in month_days_names:
    print(f'{month_name}有{month_days_names[month_name]}天')
else:
    print("输入的月份名称无效，请检查输入格式。")
