#三角形可以根据其边长分为等边三角形、等腰三角形或不等边三角形。等边三角形的三条边长度相等。等腰三角形的两条边长度相等，而第三条边的长度不相等。如果所有边的长度都不相等，那么这个三角形就是不等边三角形。
#编写一个程序，从用户那里读取三角形的三条边长。然后显示一条声明三角形类型的消息。
a = int(input("三角形的a边"))
b = int(input("三角形的b边"))
c = int(input("三角形的c边"))

if (a + b <= c) or (a + c <= b) or (b + c <= a):
    print("错误：这三条边长不能构成一个有效的三角形。")
    print("（提示：任意两边之和必须大于第三边）")

else:
    if a == b == c:
        print("是等边三角形")
    elif a == c or a == b or b == c:
        print("是等腰三角形")
    else:
        print("不等边三角形")
