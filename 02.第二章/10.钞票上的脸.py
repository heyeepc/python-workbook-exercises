#编写一个程序，从读取用户的钞票面额开始。然后，程序应该显示出现在对应钞票面额上的人名。如果不存在这样的钞票，则应该显示适当的错误消息。
banknote_person = {
    1: "华盛顿",
    2: "杰斐逊",
    5: "林肯",
    10: "汉密尔顿",
    20: "杰克逊",
    50: "格兰特",
    100: "富兰克林",
}

# 确保用户输入是整数，并捕获可能的非数字输入错误
try:
    # input() 函数读取用户输入，int() 尝试将其转换为整数
    denomination = int(input("请输入面额（例如 1, 5, 20）："))
except ValueError:
    print("输入错误：请输入一个有效的数字面额。")
    # 如果不是有效的数字，则直接退出程序
    exit()

# 使用字典的 .get() 方法进行查找
# .get() 的优势在于：如果面额存在，它返回对应的人名；
# 如果面额不存在，它返回我们指定的第二个参数 ("不存在（不流通）")，
# 这样就无需使用 if/elif/else 结构来判断是否存在了。

person_name = banknote_person.get(denomination, "不存在（不流通）")

# 显示结果
print(person_name)
