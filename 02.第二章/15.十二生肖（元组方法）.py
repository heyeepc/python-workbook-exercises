# 中国的十二生肖以12年为一个周期。下表显示了一个12年的周期。2012年是龙年，1999年是兔年
# 编写一个程序，读取用户输入的年份，并显示与那年相关的生肖。程序应该在大于或等于0的任何年份都能正常工作，而不仅是表中列出的年份。


chinese_zodiac = [
    "龙", "蛇", "马", "羊", "猴", "鸡", 
    "狗", "猪", "鼠", "牛", "虎", "兔"
]

# 确定一个参考点（基准年份），例如 2000 年是龙年
BASE_YEAR = 2000 

try:
    
    year = int(input("请输入年份 (大于公元, 如 2025): "))

    if year < 0:
        print("年份必须大于等于 0 (公元元年或以后)。")
    else:
        # 3. 计算年份差值
        # Python 的 % 运算符对于正数和负数的行为都很符合这个周期性计算
        # (例如: (1999 - 2000) % 12 = -1 % 12 = 11, 对应 '兔' (索引 11))
        # (例如: (2000 - 2000) % 12 = 0, 对应 '龙' (索引 0))
        # (例如: (2025 - 2000) % 12 = 25 % 12 = 1, 对应 '蛇' (索引 1))
      
        # 计算生肖在列表中的索引
        index = (year - BASE_YEAR) % 12
        
        #  根据索引获取并打印生肖
        print(f"您输入的年份 {year} 是: {chinese_zodiac[index]} 年")

except ValueError:
    print("输入无效。请输入一个有效的整数年份。")

