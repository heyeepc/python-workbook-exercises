# 编写一个程序，首先读取用户输入的字母等级。然后程序应该计算和显示相等数量的绩点。确保程序在用户输入无效字母等级时生成适当的错误消息。


# 正确的绩点和字母等级映射：以字母等级为键，绩点为值
LETTER_TO_GPA = {
    "A+": 4.0,
    "A": 4.0,   # 根据常见 GPA 规则，A 和 A+ 有时绩点相同
    "A-": 3.7,
    "B+": 3.3,
    "B": 3.0,
    "B-": 2.7,
    "C+": 2.3,
    "C": 2.0,
    "C-": 1.7,
    "D+": 1.3,
    "D": 1.0,
    "F": 0.0,
}

# 1. 读取用户输入的字母等级
letter = input("请输入字母等级（例如 A+、B、F）：").strip().upper()

# 2. 计算和显示相等的绩点，并处理无效输入

# 使用 dict.get() 方法查找绩点。
# 如果用户输入的 letter 在字典中找不到，则 get() 返回 None。
gpa = LETTER_TO_GPA.get(letter)

if gpa is not None:
    # 找到了对应的绩点
    print(f"字母等级 '{letter}' 对应的绩点（Grade Point Average）是：{gpa}")
else:
    # 找不到对应的绩点，显示错误消息
    print(f"错误：您输入了无效的字母等级 '{letter}'。")
    print("有效的字母等级包括：A+, A, A-, B+, B, B-, C+, C, C-, D+, D, F")
