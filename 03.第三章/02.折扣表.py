# 某零售商正在以六折的价格出售多种已停产的产品。零售商希望帮助顾客确定商品的降价，方法是在货架上放一张打印好的折扣表，显示商品的原价和打折后的价格。
# 编写一个程序，使用循环来生成这个表，显示原始价格、折扣金额和购买产品的新价格$4.95、$9.95、$14.95、$19.95 和$24.95。确保折扣金额和新价格在显示时四舍五入到小数点后两位。
a = [4.95, 9.95, 14.95, 24.95]
z = map(lambda x: x * 0.6, a)
b = list(z)
result = [a - b for a, b in zip(a, b)]

# 使用列表推导式格式化每个数字到两位小数
a_fmt = [f"{x:.2f}" for x in a]
b_fmt = [f"{x:.2f}" for x in b]
result_fmt = [f"{x:.2f}" for x in result]

print(f"原始价格: {a_fmt}")
print(f"新价格: {b_fmt}")
print(f"折扣价格: {result_fmt}")
