# 编写一个程序，将一个十进制数（以10为基数）转换为二进制数（以2为基数）。将用户输入的十进制数作为整数读取，然后使用如下所示的除法算法执行转换。
# 当算法完成时，结果包含数字的二进制表示。显示结果以及适当的消息。
n = int(input("输入十进制整数: "))
if n == 0:
    print("0")
else:
    bits = []
    while n > 0:
        bits.append(str(n % 2))  # 余数（0 或 1）
        n //= 2                  # 整除 2
    print("".join(bits[::-1]))    # 倒序输出
