# 本练习将创建一个程序，来识别用户输入的字符串中的所有单词。首先编写一个函数，该函数将字符串作为唯一的参数。函数应该返回字符串中的单词列表，并删除单词边缘的标点符号。
# 必须考虑的标点符号包括逗号、句号、问号、连字符、撇号、感叹号、冒号和分号。不要删除出现在单词中间的标点符号，例如构成缩写的撇号。
# 举个例子，如果给函数提供了字符串“Contractions include：don't，isn't，and wouldn't”，那么函数应该返回列表["Contractions"，"include"，"don't"，"isn't"，"and"，"wouldn't"]。

